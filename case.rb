weather = 'tornado'

case weather
	when 'sunny'
		puts "Grab your sunglasses or a hat."
	when 'cloudy'
		puts "Take an umbrella just in case."
	when 'rainy'
		puts "Wear your raincoat. "
	when 'windy'
		puts "Batten down the hatches!"
	when 'snowy'
		puts "Watch for icy roads!"
	when 'stormy'
		puts "Be ready for flash floods!"
	else 
		puts "Check with your local news for advice."
	end